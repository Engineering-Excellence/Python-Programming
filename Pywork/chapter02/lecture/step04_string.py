# 2.4 문자열(String)

# 문자열 유형
oneLine = 'this is one line string'
print(oneLine)

print('-' * 100)

multiLine = '''this is
multi line
string'''
print(multiLine)

print('-' * 100)

multiLine2 = 'this is\nmulti line\nstring too'
print(multiLine2)

print('=' * 100)

