def add(x: int, y: int) -> int:
    """
    덧셈 함수

    :param x: 정수
    :type x: int
    :param y: 정수
    :type y: int
    :return: 두 정수의 합
    """
    return x + y


def sub(x: int, y: int) -> int:
    """
    뺄셈 함수

    :param x: 정수
    :type x: int
    :param y: 정수
    :type y: int
    :return: 두 정수의 차
    """
    return x - y


def mul(x: int, y: int) -> int:
    """
    곱셈 함수

    :param x: 정수
    :type x: int
    :param y: 정수
    :type y: int
    :return: 두 정수의 곱
    """
    return x * y


def div(x: int, y: int) -> float:
    """
    나눗셈 함수

    :param x: 정수
    :type x: int
    :param y: 정수
    :type y: int
    :return: x를 y로 나눈 값
    """
    return x / y
